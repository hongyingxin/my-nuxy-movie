<script setup>
  // Ë∑ØÁî±ÂèÇÊï∞
  // API ÂØºÂÖ•
  import { getPersonDetail, getPersonCredits } from '~/api/person'

  const route = useRoute()
  const actorId = parseInt(route.params.id)

  // Êï∞ÊçÆËé∑Âèñ
  const detail = getPersonDetail(actorId)
  const credits = getPersonCredits(actorId)
  console.log('detail-------', detail)
  console.log('credits-------', credits)

  // ÂìçÂ∫îÂºèÊï∞ÊçÆ
  const activeTab = ref('all')
  const scrollContainer = ref(null)
  const scrollPosition = ref(0)
  const maxScroll = ref(0)
  const scrollProgress = ref(0)

  // ËÆ°ÁÆóÂ±ûÊÄß
  const filteredWorks = computed(() => {
    if (!credits.data.value?.cast) return []

    const works = credits.data.value.cast
    switch (activeTab.value) {
      case 'movie':
        return works.filter(work => work.media_type === 'movie').slice(0, 20)
      case 'tv':
        return works.filter(work => work.media_type === 'tv').slice(0, 20)
      default:
        return works.slice(0, 20)
    }
  })

  // Êó∂Èó¥ËΩ¥Êï∞ÊçÆ
  const timelineData = computed(() => {
    if (!credits.data.value?.cast) return []

    // Ëé∑ÂèñÊâÄÊúâ‰ΩúÂìÅÂπ∂ÊåâÂπ¥‰ªΩÂàÜÁªÑ
    const works = credits.data.value.cast
    const yearGroups = {}

    works.forEach(work => {
      const date = work.release_date || work.first_air_date
      if (date) {
        const year = date.split('-')[0]
        if (!yearGroups[year]) {
          yearGroups[year] = []
        }
        yearGroups[year].push(work)
      }
    })

    // ËΩ¨Êç¢‰∏∫Êï∞ÁªÑÂπ∂ÊåâÂπ¥‰ªΩÊéíÂ∫èÔºà‰ªéÊñ∞Âà∞ÊóßÔºâ
    return Object.entries(yearGroups)
      .map(([year, works]) => ({
        year: parseInt(year),
        works: works.sort((a, b) => {
          const dateA = a.release_date || a.first_air_date
          const dateB = b.release_date || b.first_air_date
          return new Date(dateB) - new Date(dateA)
        }),
      }))
      .sort((a, b) => b.year - a.year) // ÊåâÂπ¥‰ªΩÈôçÂ∫èÊéíÂàó
  })

  // SEO ÈÖçÁΩÆ
  useHead(() => ({
    title: detail.data.value
      ? `${detail.data.value.name} - ÊºîÂëòËØ¶ÊÉÖ - Nuxt Movie`
      : 'ÊºîÂëòËØ¶ÊÉÖ - Nuxt Movie',
    meta: [
      {
        name: 'description',
        content: detail.data.value?.biography || 'Êü•ÁúãÊºîÂëòËØ¶ÁªÜ‰ø°ÊÅØÂíå‰ΩúÂìÅ',
      },
    ],
  }))

  // ÂØºËà™Âà∞‰ΩúÂìÅËØ¶ÊÉÖÈ°µ
  const navigateToWork = work => {
    const mediaType = work.media_type || 'movie'
    navigateTo(`/${mediaType}/${work.id}`)
  }

  // Âà∑Êñ∞ÂäüËÉΩ
  // const refresh = () => {
  //   detail.refresh()
  //   credits.refresh()
  // }

  // ÊªöÂä®Áõ∏ÂÖ≥ÊñπÊ≥ï
  const handleScroll = () => {
    if (!scrollContainer.value) return

    const container = scrollContainer.value
    scrollPosition.value = container.scrollLeft
    maxScroll.value = container.scrollWidth - container.clientWidth

    // ËÆ°ÁÆóÊªöÂä®ËøõÂ∫¶ÁôæÂàÜÊØî
    if (maxScroll.value > 0) {
      scrollProgress.value = (scrollPosition.value / maxScroll.value) * 100
    } else {
      scrollProgress.value = 0
    }
  }

  const scrollLeft = () => {
    if (!scrollContainer.value) return
    const container = scrollContainer.value
    container.scrollBy({ left: -400, behavior: 'smooth' })
  }

  const scrollRight = () => {
    if (!scrollContainer.value) return
    const container = scrollContainer.value
    container.scrollBy({ left: 400, behavior: 'smooth' })
  }

  const scrollToIndex = index => {
    if (!scrollContainer.value) return
    const container = scrollContainer.value
    const itemWidth = 200 + 16 // 200px ÂÆΩÂ∫¶ + 16px gap
    const scrollTo = index * 5 * itemWidth // ÊØèÈ°µÊòæÁ§∫5‰∏™È°πÁõÆ
    container.scrollTo({ left: scrollTo, behavior: 'smooth' })
  }

  const getIndicatorClass = index => {
    if (!scrollContainer.value) return 'bg-gray-300'

    // const container = scrollContainer.value
    const itemWidth = 200 + 16
    const currentIndex = Math.floor(scrollPosition.value / (5 * itemWidth))

    return index === currentIndex
      ? 'bg-red-600'
      : 'bg-gray-300 hover:bg-gray-400'
  }

  // ÁõëÂê¨Ê†áÁ≠æÂàáÊç¢ÔºåÈáçÁΩÆÊªöÂä®‰ΩçÁΩÆ
  watch(activeTab, () => {
    if (scrollContainer.value) {
      scrollContainer.value.scrollLeft = 0
      scrollPosition.value = 0
      scrollProgress.value = 0
    }
  })

  // ÁªÑ‰ª∂ÊåÇËΩΩÂêéÂàùÂßãÂåñÊªöÂä®Áä∂ÊÄÅ
  onMounted(() => {
    nextTick(() => {
      handleScroll()
    })
  })
</script>

